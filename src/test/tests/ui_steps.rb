require 'forwardable'

class UiSteps
  extend Forwardable

  attr_reader :driver, :root_view, :reset_url, :home_url

  def_delegators :root_view, :view, :action, :text, :list, :text_field, :dropdown_field, :checkbox_field

  def initialize(driver, root_view, reset_url, home_url)
    @driver = driver
    @root_view = root_view
    @reset_url = reset_url
    @home_url = home_url
  end

  def navigate_to_reset
    driver.navigate.to(reset_url)
    view('reset').is_visible!
  end

  def navigate_to_home
    driver.navigate.to(home_url)
    view('home').is_visible!
  end

end
